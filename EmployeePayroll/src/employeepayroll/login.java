    public void updateDateTime() {
        // Timer to update the time every second (1000 milliseconds)
        Timer timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Run on the Event Dispatch Thread to ensure thread-safety
                SwingUtilities.invokeLater(() -> {
                    Calendar cal = new GregorianCalendar();
                    int year = cal.get(Calendar.YEAR);
                    int month = cal.get(Calendar.MONTH);
                    int day = cal.get(Calendar.DAY_OF_MONTH);

                    date_menu.setText((month + 1) + "/" + day + "/" + year);

                    int second = cal.get(Calendar.SECOND);
                    int minute = cal.get(Calendar.MINUTE);
                    int hour = cal.get(Calendar.HOUR_OF_DAY);  // Use HOUR_OF_DAY for 24-hour format
                    time_menu.setText(String.format("%02d:%02d:%02d", hour, minute, second));  // Format to HH:mm:ss
                });
            }
        });

        timer.setInitialDelay(0);  // Start immediately
        timer.start();
    }
