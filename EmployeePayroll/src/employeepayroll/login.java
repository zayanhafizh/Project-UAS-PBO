    private void login_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_buttonActionPerformed
        // TODO add your handling code here:
        String sql = "SELECT id, username, password, division FROM Users WHERE (username = ? AND password = ?)";
        
        try {
        int count = 0;
        
        // Prepare the SQL statement
        pst = conn.prepareStatement(sql);
        
        // Set parameters
        pst.setString(1, username_txt.getText());
        pst.setString(2, new String(password_txt.getPassword()));  // Using JPasswordField
        
        // Execute query
        rs = pst.executeQuery();
        
        // Process the result set
        while (rs.next()) {
            int id =rs.getInt(1);
            Employee.id = id;
            count++;
            
        }
        
        String access = divisi_combo.getSelectedItem().toString();
        
        if ("Admin".equals(access)) {  // Corrected comparison
            if (count == 1) {
                JOptionPane.showMessageDialog(null, "Success");
                MainMenu j = new MainMenu();
                j.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Invalid login or user not found.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Access denied. Admins only.");
        }
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close resources
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Resource Closing Error", JOptionPane.ERROR_MESSAGE);
            }
        } 
    }//GEN-LAST:event_login_buttonActionPerformed

